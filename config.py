# Контрольные точки сохранения состояния обучения весов нейросети
GENERATOR_LAST_PATH = str(CHECKPOINT_DIR / 'generator_last.pt')   # Последнее зафиксированное состояние генератора
GENERATOR_BEST_PATH = str(CHECKPOINT_DIR / 'generator_best.pt')   # Лучшее по валидации CE состояние генератора
