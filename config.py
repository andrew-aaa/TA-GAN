# config.py
"""
Модуль глобальной конфигурации и гиперпараметров нейросетевого пайплайна TA-GAN.

Содержит все ключевые константы, управляющие процессами токенизации, архитектурными 
параметрами генератора и дискриминатора, весовыми коэффициентами многокомпонентных 
функций потерь (включая физико-химические дескрипторы белков), а также конфигурациями 
директорий ввода-вывода и режимами инференса.
"""

from __future__ import annotations

from pathlib import Path
import torch

# ==============================================================================
# СИСТЕМНЫЕ ДИРЕКТОРИИ И ПУТИ ИНФРАСТРУКТУРЫ
# ==============================================================================
BASE_DIR = Path(__file__).resolve().parent
DATA_DIR = BASE_DIR / 'data'
CHECKPOINT_DIR = BASE_DIR / 'checkpoints'
LOG_DIR = BASE_DIR / 'logs'

# Гарантированное создание структуры папок при инициализации конфигурации
CHECKPOINT_DIR.mkdir(parents=True, exist_ok=True)
LOG_DIR.mkdir(parents=True, exist_ok=True)

# ==============================================================================
# ПАРАМЕТРЫ ПОСЛЕДОВАТЕЛЬНОСТЕЙ И ТОКЕНИЗАЦИИ (БИОЛОГИЧЕСКИЙ АЛФАВИТ)
# ==============================================================================
MAX_AA_LEN = 256  # Максимальная длина нативной аминокислотной цепи антидота
MAX_LEN = MAX_AA_LEN + 2  # Полная длина тензора с учетом технических токенов (BOS, EOS)

# Служебные токены выравнивания и разметки границ последовательности
PAD_TOKEN = '_'  # Токен дополнения (Padding) до статического размера тензора
BOS_TOKEN = '^'  # Токен начала последовательности (Beginning of Sequence)
EOS_TOKEN = '*'  # Токен завершения авторегрессионного декодирования (End of Sequence)

# Стандартный алфавит из 20 канонических L-аминокислот
AMINO_ACIDS = 'ACDEFGHIKLMNPQRSTVWY'

# Итоговый упорядоченный словарь (Vocabulary) модели
VOCAB = PAD_TOKEN + BOS_TOKEN + EOS_TOKEN + AMINO_ACIDS
VOCAB_SIZE = len(VOCAB)

# Сквозные глобальные индексы токенов для быстрой векторной фильтрации
PAD_IDX = VOCAB.index(PAD_TOKEN)
BOS_IDX = VOCAB.index(BOS_TOKEN)
EOS_IDX = VOCAB.index(EOS_TOKEN)
AA_START_IDX = VOCAB.index('A')  # Граница перехода от служебных токенов к аминокислотам

# ==============================================================================
# АРХИТЕКТУРА НЕЙРОСЕТЕВЫХ МОДЕЛЕЙ И ЭМБЕДДЕРОВ
# ==============================================================================
# Идентификатор базовой предобученной большой языковой модели белков (Protein LLM)
ESM_MODEL_NAME = "facebook/esm2_t30_150M_UR50D"
# ESM_MODEL_NAME = "facebook/esm2_t33_650M_UR50D - другая модель

# Конфигурация эффективного дообучения параметров (Parameter-Efficient Fine-Tuning)
USE_LORA = True       # Флаг применения низкоранговой адаптации LoRA к трансформеру ESM-2
LORA_R = 8            # Внутренний ранг декомпозиции матриц адаптации
LORA_ALPHA = 16       # Коэффициент масштабирования градиентов LoRA
LORA_DROPOUT = 0.05   # Вероятность зануления активаций в адаптивных слоях

# Размерности скрытых пространств и проекций
ESM_OUTPUT_DIM = 512  # Исходная размерность эмбеддингов на выходе проекционного слоя ESM-2
CONDITION_DIM = 512   # Размерность вектора биологического условия (контекст токсина)
LATENT_DIM = 64       # Размерность стохастического шума z для обеспечения разнообразия генерации

# Архитектурные гиперпараметры Генератора (Transformer Decoder)
EMBED_DIM = 256       # Размерность токеновых эмбеддингов декодера
NUM_HEADS = 8         # Количество голов в механизме многоголового внимания (Multi-Head Attention)
NUM_LAYERS = 4        # Число блоков декодера трансформера
FF_MULT = 4           # Множитель размерности промежуточного слоя Feed-Forward (256 * 4 = 1024)
DROPOUT = 0.1         # Регуляризационный дропаут скрытых слоев генератора

# Архитектурные гиперпараметры Дискриминатора (Transformer Encoder)
DISC_EMBED_DIM = 256  # Размерность латентного представления дискриминатора
DISC_NUM_HEADS = 8    # Количество голов внимания рецептивного поля
DISC_NUM_LAYERS = 3   # Количество слоев кодировщика, оценивающих валидность цепи
DISC_DROPOUT = 0.1    # Регуляризационный дропаут дискриминатора
PROJECTION_DIM = 128  # Размерность финальной проекции головы бинарного классификатора

# ==============================================================================
# РЕЖИМЫ И ГИПЕРПАРАМЕТРЫ ПРОЦЕССА ОБУЧЕНИЯ (TRAINING HPARAMS)
# ==============================================================================
DEVICE = 'cuda' if torch.cuda.is_available() else 'cpu'  # Автоматический выбор вычислительного устройства
BATCH_SIZE = 16       # Размер пакета данных (мини-батча)
EPOCHS = 100          # Полное количество эпох состязательного обучения

# Специфические параметры оптимизации шагов Adversarial WGAN
CRITIC_ITERATIONS = 5  # Количество шагов оптимизации дискриминатора на один шаг генератора
GRAD_CLIP = 5.0        # Порог жесткого отсечения градиентов (Gradient Clipping) для подавления их взрыва

# Профили скоростей обучения (Learning Rates) и регуляризаторы
LR_G = 2e-4            # Стартовый шаг сходимости оптимизатора Adam для Генератора
LR_D = 1e-4            # Стартовый шаг сходимости оптимизатора Adam для Дискриминатора
BETAS = (0.5, 0.9)     # Коэффициенты импульса (beta1, beta2) для стабилизации WGAN-динамики
WEIGHT_DECAY = 1e-4    # Коэффициент L2-регуляризации весов

# Динамическое расписание фаз включения регуляризаторов и состязания
GENERATOR_PRETRAIN_EPOCHS = 15  # Длительность фазы чистого MLM/CE-претрейна (Teacher Forcing)
BIO_LOSS_RAMP_EPOCH = 20        # Эпоха плавного старта дифференцируемых биологических ограничений
ADV_WEIGHT_MAX = 0.5            # Максимальный целевой вес состязательного лосса (Adversarial Loss)
LENGTH_REG_WEIGHT = 0.10        # Весовой коэффициент штрафа за ошибку предсказания длины белка
LABEL_SMOOTHING = 0.0           # Сглаживание меток

# Коэффициент состязательной регуляризации Липшица
GP_WEIGHT = 10.0                # Вес штрафа за градиент (Gradient Penalty) в схеме WGAN-GP

# ==============================================================================
# ПАРАМЕТРЫ ИНФЕРЕНСА, СТОХАСТИЧНОСТИ И ВАЛИДАЦИИ
# ==============================================================================
GENERATION_TEMPERATURES = [0.2, 0.5, 0.7, 1.0, 1.2]  # Мультимодальная сетка температур для стохастического сэмплинга
NUM_GENERATION_ATTEMPTS = 5                          # Число итераций генерации на одну температуру для жесткого скрининга

# Внутренние весовые коэффициенты суррогатной функции качества кандидата (Inference Score)
INFERENCE_LEN_MAE_WEIGHT = 0.03        # Штраф за девиацию реальной длины от предсказанной идеальной
INFERENCE_EOS_ERROR_WEIGHT = 1.00      # Штраф за генерацию без терминирующего стоп-кодона
INFERENCE_AA_KL_WEIGHT = 0.20          # Штраф за отклонение частот аминокислот от нативного распределения
INFERENCE_REPEAT_WEIGHT = 0.20         # Штраф за локальные монотонные дубликаты (AA-повторы)

# Параметры экспоненциального сглаживания весов и дифференцируемой релаксации
EMA_DECAY = 0.999    # Коэффициент сохранения исторической памяти весов в схеме EMA
TAU_START = 1.0      # Начальное значение температуры Gumbel-Softmax на старте обучения
TAU_END = 0.5        # Финальное значение температуры Gumbel-Softmax (повышение детерминированности)

# Параметры подсистемы ввода-вывода (I/O) и логирования
NUM_WORKERS = 2             # Количество параллельных потоков процессора для сборки батчей
PIN_MEMORY = True           # Фиксация памяти в RAM для ускорения пересылки данных на GPU
RESET_METRICS_CSV = True    # Флаг полной перезаписи лог-файла метрик перед началом новой сессии
EMPTY_CACHE_EACH_EPOCH = False  # Принудительная очистка неиспользуемой VRAM памяти CUDA каждую эпоху

# ==============================================================================
# ГИПЕРПАРАМЕТРЫ БИОЛОГИЧЕСКИХ ФУНКЦИЙ ПОТЕРЬ (BIOLOGICAL GUIDANCE LOSS)
# ==============================================================================
# Коэффициенты баланса вклада отдельных физико-химических свойств в общий биологический лосс
BIO_HYDRO_WEIGHT = 0.05     # Вес оптимизации гидрофобного профиля белков
BIO_PI_WEIGHT = 0.05        # Вес удержания изоэлектрической точки в физиологическом диапазоне
BIO_AMP_WEIGHT = 0.08       # Вес оптимизации амфипатического момента (индекс амфипатичности альфа-спиралей)

# Физико-химические целевые референсные коридоры
BIO_HYDRO_THRESHOLD = 0.45  # Минимально допустимый нормированный уровень общей гидрофобности антидота
BIO_PI_MIN = 5.5            # Нижний терапевтический порог изоэлектрической точки (pI)
BIO_PI_MAX = 9.5            # Верхний терапевтический порог изоэлектрической точки (pI)

# ==============================================================================
# ПУТИ К ФАЙЛАМ ДАННЫХ И КОНЕЧНЫМ ЧЕКПОИНТАМ (ПАТТЕРНЫ ХРАНЕНИЯ)
# ==============================================================================
TOXIN_FASTA_PATH = str(DATA_DIR / 'toxins_paired.fasta')          # Провалидированные пары токсинов (очищенные)
