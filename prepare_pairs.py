# prepare_pairs.py
"""
Модуль первичной фильтрации и формирования валидных пар «токсин-антитоксин».

Скрипт выполняет синтаксический анализ исходных биологических файлов в формате FASTA,
используя регулярные выражения для извлечения числовых идентификаторов белков. 
Затем он производит строгое пересечение (matching) множеств по совпадению индексов 
и генерирует очищенные парные наборы данных, готовые для последующего извлечения 
эмбеддингов и обучения GAN.
"""

from __future__ import annotations

import re
from Bio import SeqIO

from config import RAW_TOXIN_FASTA_PATH, RAW_ANTITOXIN_FASTA_PATH, TOXIN_FASTA_PATH, ANTITOXIN_FASTA_PATH


def main():
    """
    Основная управляющая функция для сопоставления и сборки пар токсинов и антитоксинов.

    Считывает сырые файлы экспрессированных белков, группирует их во внутренние хеш-таблицы
    по порядковым номерам систем Type II TA и экспортирует пересекающиеся записи.

    Raises:
        FileNotFoundError: Если один из сырых FASTA-файлов отсутствует по указанному пути.
        RuntimeError: Если в процессе сопоставления не удалось собрать ни одной пары.
    """
    # Инициализация временных ассоциативных словарей для индексации объектов SeqRecord
    toxins = {}
    antitoxins = {}
    
    # КРИТИЧЕСКИЙ ЭТАП: Компиляция регулярных выражений для парсинга заголовков.
    # Шаблоны ищут строгое соответствие вида "T123" или "AT123", где число — ID оперона.
    t_re = re.compile(r'^T(\d+)$')
    at_re = re.compile(r'^AT(\d+)$')

    # Чтение и индексация сырого файла экспериментальных токсинов
    for record in SeqIO.parse(RAW_TOXIN_FASTA_PATH, 'fasta'):
        # Извлечение первого токена из строки заголовка (идентификатор без описания)
        name = record.id.split()[0]
        m = t_re.match(name)
        if m:
            # Запись в словарь под уникальным строковым ключом числа (например, "123")
            toxins[m.group(1)] = record

    # Чтение и индексация сырого файла экспериментальных антитоксинов
    for record in SeqIO.parse(RAW_ANTITOXIN_FASTA_PATH, 'fasta'):
        # Извлечение первого токена из строки заголовка
        name = record.id.split()[0]
        m = at_re.match(name)
        if m:
            # Запись в словарь под уникальным строковым ключом числа
            antitoxins[m.group(1)] = record

    # Списки для накопления валидированных парных биоинформатических записей
    paired_toxins, paired_antitoxins = [], []
    
    # АЛГОРИТМ СОПОСТАВЛЕНИЯ: Итерирование по проиндексированным токсинам и поиск пары
    for number, t_record in toxins.items():
        # Поиск антитоксина с точно таким же числовым идентификатором системы
        a_record = antitoxins.get(number)
        
        # Если антитоксин найден, пара считается валидной и добавляется в выборку
        if a_record is not None:
            paired_toxins.append(t_record)
            paired_antitoxins.append(a_record)

    # Вывод диагностической информации о размере сформированного датасета
    print(f'[prepare pairs] Найдено пар: {len(paired_toxins)}')
    
    # Защитная проверка: остановка выполнения, если пересечение множеств оказалось пустым
    if len(paired_toxins) == 0:
        print("[prepare pairs] Предупреждение: Не найдено ни одной общей пары. Проверьте форматы ID.")
    
    # Сериализация отфильтрованных и выровненных последовательностей обратно в FASTA-структуру.
    # Порядок элементов в обоих файлах теперь строго синхронизирован по индексам.
    SeqIO.write(paired_toxins, TOXIN_FASTA_PATH, 'fasta')
    SeqIO.write(paired_antitoxins, ANTITOXIN_FASTA_PATH, 'fasta')


if __name__ == '__main__':
    main()